package com.alvaro;

public class Alumno {
    private String nombre;
    private int edad;

    private String dni;

    public Alumno(String nombre, int edad, String dni) {

        this.nombre = nombre;
        this.edad = edad;
        this.dni = dni;
    }

    public String getNombre() {

        return nombre;
    }

    public int getEdad() {
        return edad;
    }

    public String getDni() {
        return dni;
    }

}




















